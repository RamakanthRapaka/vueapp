<template>
<div>
<h1>Register</h1>
<input type="text" id="email" name="email" placeholder="EMAIL" value="" v-model="email" />
<br>
<input type="password" id="password" name="password" placeholder="PASSWORD" value="" v-model="password" />
<br>
<button type="submit" @click="register">LOGIN</button>
</div>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService';
export default {
  data () {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    async register () {
     const response =  await AuthenticationService.register({
        email: this.email,
        password: this.password
      })
      console.log(response.data);
    }
  },
  mounted() {
    setTimeout(() => {
      this.email = 'hello world'
    }, 2000)
  },
}
</script>
